package com.interview.entity.constance;

import org.springframework.security.core.authority.SimpleGrantedAuthority;

import java.util.Set;
import java.util.stream.Collectors;

public enum Role {
    ADMIN(Set.of(Permission.QUESTION_DELETE, Permission.QUESTIONS_ADD, Permission.QUESTIONS_READ, Permission.USERS_DELETE, Permission.USERS_BAN)),
    USER(Set.of(Permission.QUESTION_DELETE, Permission.QUESTIONS_ADD, Permission.QUESTIONS_READ));

    private final Set<Permission> permissions;

    Role(Set<Permission> permissions) {
        this.permissions = permissions;
    }

    public Set<Permission> getPermissions() {
        return permissions;
    }

    public Set<SimpleGrantedAuthority> getAuthorities() {
        return getPermissions().stream()
                .map(permission -> new SimpleGrantedAuthority(permission.name()))
                .collect(Collectors.toSet());
    }
}
